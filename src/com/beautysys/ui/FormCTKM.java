    private void FillToForm(int i) {
        KhuyenMai km = dao.select().get(i);
        txtMaKM.setText(km.getMaKM());
        txtTenKM.setText(km.getTenKM());
        txtMoTa.setText(km.getMoTa());
        txtNgayBD.setText(DateHelper.toString(km.getNgayBD(), "dd/MM/yyyy"));
        txtNgayKT.setText(DateHelper.toString(km.getNgayKT(), "dd/MM/yyyy"));
        txtPhanTramGG.setText(String.valueOf(km.getPhanTramGG()));
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhuyenMai.getModel();
        model.setRowCount(0);
        try {
            List<KhuyenMai> list = dao.select();
            for (KhuyenMai th : list) {
                Object[] row = {
                    th.getMaKM(),
                    th.getTenKM(),
                    th.getMoTa(),
                    DateHelper.toString(th.getNgayBD()),
                    DateHelper.toString(th.getNgayKT()),
                    th.getPhanTramGG(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private void Xoa() {
        if (DialogHelper.confirm(this, "Bạn thực sự muốn xóa sản phẩm này?")) {
            try {
                dao.delete(txtMaKM.getText());
                this.fillTable();

                DialogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                DialogHelper.alert(this, "Xóa thất bại!");
            }
        }
    }
