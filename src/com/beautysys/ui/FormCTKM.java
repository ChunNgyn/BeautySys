    KhuyenMai getModel() {
        KhuyenMai model = new KhuyenMai();
        model.setMaKM(txtMaKM.getText());
        model.setTenKM(txtTenKM.getText());
        model.setMoTa(txtMoTa.getText());
        model.setNgayBD(DateHelper.toDate(txtNgayBD.getText()));
        model.setNgayKT(DateHelper.toDate(txtNgayKT.getText()));
        model.setPhanTramGG(Float.parseFloat(txtPhanTramGG.getText()));

        return model;
    }

    void them() {
        KhuyenMai model = getModel();
        try {
            dao.insert(model);
            this.fillTable();

            DialogHelper.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Thêm mới thất bại!");
        }
    }

    void LamMoi() {
        int nextId = dao.select().size() + 1;
        String maKM;
        if (nextId < 10) {
            maKM = "KM00" + nextId;
        } else if (nextId < 100) {
            maKM = "KM0" + nextId;
        } else {
            maKM = "KM" + nextId;
        }
        txtMaKM.setText(maKM);
        txtTenKM.setText("");
        txtMoTa.setText("");
        txtNgayBD.setText("");
        txtNgayKT.setText("");
        fillTable();
    }

